#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main(int c, char **v)
{
	while (--c > 0)
		ft_putstr(v[c]);
	return (0);
}
